    public static int[] returnLongestSub(int[] nums){
        int[] isSorted = new int[nums.length];
        int maxLong =-1;
        int startIndex=0;
        int endIndex=0;
        boolean isChain=false;
        int curCounter=0;

        int curStart=0;
        int curEnd =0;
        for(int i =0; i<nums.length-1;i++){
            if(i==nums.length-2){
                if(nums[i]<nums[i+1]){
                    isSorted[i]=1;
                    isSorted[i+1]=1;
                    curCounter+=2;
                    curEnd=i+1;
                    if(curCounter>maxLong){
                        startIndex=curStart;
                        endIndex = curEnd;
                    }
                }
            }else{
                if(nums[i]<nums[i+1] && isChain){
                    isSorted[i]=1;
                    curCounter++;
                }else if(nums[i]<nums[i+1]){
                    isSorted[i]=1;
                    curCounter++;
                    isChain=true;
                    curStart=i;
                }else if(nums[i]>nums[i+1]){
                    isSorted[i]=0;
                    isChain=false;
                    curEnd=i;
                    if(curCounter>maxLong){
                        startIndex=curStart;
                        endIndex = curEnd;
                    }
                }

            }
        }
        return Arrays.copyOfRange(nums, startIndex, endIndex+1);
    }
